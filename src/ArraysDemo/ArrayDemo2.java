package ArraysDemo;

public class ArrayDemo2 {

    public static void main(String[] args) {

        char[] c = new char[5];

        c[0] = 'i';
        c[1] = 'd';
        c[2] = 'f';
        c[3] = 'h';
        c[4] = 'q';

        for(int i=0 ; i<5 ;i++ )
           System.out.println(c[i]);

    }
}
